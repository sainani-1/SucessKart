  async function requestCameraMicPermissions() {
    if (!navigator.mediaDevices?.getUserMedia) {
      setErrorMsg("This browser does not support camera/microphone access.");
      return null;
    }

    try {
      const stream = await navigator.mediaDevices.getUserMedia({
        video: true,
        audio: true,
      });
      return stream;
    } catch (error) {
      setErrorMsg(
        "Camera and microphone permissions are required to start the exam."
      );
      return null;
    }
  }

  async function requestScreenSharePermissions() {
    if (!navigator.mediaDevices?.getDisplayMedia) {
      setErrorMsg("Entire-screen sharing is required, but this browser does not support it.");
      return null;
    }

    try {
      const stream = await navigator.mediaDevices.getDisplayMedia({
        video: {
          displaySurface: "monitor",
        },
        audio: true,
      });
      if (!isEntireScreenShareStream(stream)) {
        stream.getTracks().forEach((track) => track.stop());
        setErrorMsg("Share the entire screen only. Tab share or app-window share is not allowed.");
        return null;
      }
      return stream;
    } catch {
      setErrorMsg("Entire-screen sharing is required to start this live exam. Choose Entire Screen, not a tab or app window.");
      return null;
    }
  }

